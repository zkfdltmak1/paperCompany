package com.train.vo;

public class TimeVO {
	private String time_code;
	private String time_time;
	private String arrival_time;
	
	
	public String getTime_code() {
		return time_code;
	}
	public void setTime_code(String time_code) {
		this.time_code = time_code;
	}
	public String getTime_time() {
		return time_time;
	}
	public void setTime_time(String time_time) {
		this.time_time = time_time;
	}
	public String getArrival_time() {
		return arrival_time;
	}
	public void setArrival_time(String arrival_time) {
		this.arrival_time = arrival_time;
	}
	
	
	
	
	
}
