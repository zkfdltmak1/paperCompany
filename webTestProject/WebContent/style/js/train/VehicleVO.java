package com.train.vo;

public class VehicleVO {
	
	private String vehicle_code;
	private String vehicle_kinds;
	
	
	
	public String getVehicle_code() {
		return vehicle_code;
	}
	public void setVehicle_code(String vehicle_code) {
		this.vehicle_code = vehicle_code;
	}
	public String getVehicle_kinds() {
		return vehicle_kinds;
	}
	public void setVehicle_kinds(String vehicle_kinds) {
		this.vehicle_kinds = vehicle_kinds;
	}
	
	
}
